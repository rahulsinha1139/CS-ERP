// Utility: Get template by name
export function getTemplateByName(name: string): ServiceTemplateDefinition | undefined {
  return CS_SERVICE_TEMPLATES.find((t) => t.name === name);
}

// Utility: Get all template names
export function getAllTemplateNames(): string[] {
  return CS_SERVICE_TEMPLATES.map((t) => t.name);
}

// Utility: Get templates by category
export function getTemplatesByCategory(category: string): ServiceTemplateDefinition[] {
  return CS_SERVICE_TEMPLATES.filter((t) => t.category === category);
}

